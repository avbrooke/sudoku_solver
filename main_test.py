import unittest
from main import is_valid_board, solver, validator, find_empty, print_board

class TestSudokuSolver(unittest.TestCase):

    def test_valid_board_len(self):
        valid_board = [
            [5, 3, 0, 0, 7, 0, 0, 0, 0],
            [6, 0, 0, 1, 9, 5, 0, 0, 0],
            [0, 9, 8, 0, 0, 0, 0, 6, 0],
            [8, 0, 0, 0, 6, 0, 0, 0, 3],
            [4, 0, 0, 8, 0, 3, 0, 0, 1],
            [7, 0, 0, 0, 2, 0, 0, 0, 6],
            [0, 6, 0, 0, 0, 0, 2, 8, 0],
            [0, 0, 0, 4, 1, 9, 0, 0, 5],
            [0, 0, 0, 0, 8, 0, 0, 7, 9]
        ]
        result = solver(valid_board)
        self.assertTrue(result)  # The board should be solvable
        self.assertTrue(is_valid_board(valid_board))

    def test_invalid_board_len(self):
        invalid_board = [
            [5, 3, 0, 0, 7, 0, 0, 0, 0],
            [6, 0, 0, 1, 9, 5, 0, 0, 0],
            [0, 9, 8, 0, 0, 0, 0, 6, 0],
            [8, 0, 0, 0, 6, 0, 0, 0, 3]
        ]
        self.assertFalse(is_valid_board(invalid_board))

    def test_valid_sudoku(self):
        # A valid sudoku puzzle
        board = [
            [7, 8, 0, 4, 0, 0, 1, 2, 0],
            [6, 0, 0, 0, 7, 5, 0, 0, 9],
            [0, 0, 0, 6, 0, 1, 0, 7, 8],
            [0, 0, 7, 0, 4, 0, 2, 6, 0],
            [0, 0, 1, 0, 5, 0, 9, 3, 0],
            [9, 0, 4, 0, 6, 0, 0, 0, 5],
            [0, 7, 0, 3, 0, 0, 0, 1, 2],
            [1, 2, 0, 0, 0, 7, 4, 0, 0],
            [0, 4, 9, 2, 0, 6, 0, 0, 7]
        ]
        solver(board)
        self.assertEqual(board, [
            [7, 8, 3, 4, 9, 5, 1, 2, 6],
            [6, 1, 2, 8, 7, 5, 3, 4, 9],
            [5, 9, 4, 6, 2, 1, 7, 8, 3],
            [8, 3, 7, 9, 4, 2, 5, 6, 1],
            [4, 6, 1, 7, 5, 8, 9, 3, 2],
            [9, 2, 5, 3, 6, 1, 8, 7, 4],
            [3, 7, 8, 1, 9, 4, 6, 5, 2],
            [1, 2, 6, 5, 3, 7, 4, 9, 8],
            [2, 4, 9, 2, 8, 6, 3, 1, 7]
        ])  # Expected solution after solving

    def test_invalid_sudoku(self):
        invalid_board = [
            [5, 3, 0, 0, 7, 0, 0, 0, 0],
            [6, 0, 0, 1, 9, 5, 0, 0, 0],
            [0, 9, 8, 0, 0, 0, 0, 6, 0],
            [8, 0, 0, 0, 6, 0, 0, 0, 3],
            [4, 0, 0, 8, 0, 3, 0, 0, 1],
            [7, 0, 0, 0, 2, 0, 0, 0, 6],
            [0, 6, 0, 0, 0, 0, 2, 8, 0],
            [0, 0, 0, 4, 1, 9, 0, 0, 5],
            [0, 0, 0, 0, 8, 0, 0, 7, 9]
        ]
        result = solver(invalid_board)
        self.assertFalse(result) # Should return False as it's unsolvable

    def test_find_empty(self):
        board = [
            [5, 3, 0, 0, 7, 0, 0, 0, 0],
            [6, 0, 0, 1, 9, 5, 0, 0, 0],
            [0, 9, 8, 0, 0, 0, 0, 6, 0],
            [8, 0, 0, 0, 6, 0, 0, 0, 3],
            [4, 0, 0, 8, 0, 3, 0, 0, 1],
            [7, 0, 0, 0, 2, 0, 0, 0, 6],
            [0, 6, 0, 0, 0, 0, 2, 8, 0],
            [0, 0, 0, 4, 1, 9, 0, 0, 5],
            [0, 0, 0, 0, 8, 0, 0, 7, 9]
        ]
        empty_pos = find_empty(board)
        self.assertEqual(empty_pos, (0, 2))  # First empty cell is at position (0, 2)

    def test_validator(self):
        board = [
            [5, 3, 0, 0, 7, 0, 0, 0, 0],
            [6, 0, 0, 1, 9, 5, 0, 0, 0],
            [0, 9, 8, 0, 0, 0, 0, 6, 0],
            [8, 0, 0, 0, 6, 0, 0, 0, 3],
            [4, 0, 0, 8, 0, 3, 0, 0, 1],
            [7, 0, 0, 0, 2, 0, 0, 0, 6],
            [0, 6, 0, 0, 0, 0, 2, 8, 0],
            [0, 0, 0, 4, 1, 9, 0, 0, 5],
            [0, 0, 0, 0, 8, 0, 0, 7, 9]
        ]
        # Testing for valid number placement
        valid = validator(board, (0, 2), 4)  # Testing if number 4 can be placed at position (0, 2)
        self.assertTrue(valid)

        # Testing for invalid number placement (duplicate in row)
        invalid = validator(board, (0, 2), 5)  # Testing if number 5 can be placed at position (0, 2)
        self.assertFalse(invalid)

    def test_print_board(self):
        # Just a simple test to check if print_board runs without error
        board = [
            [5, 3, 0, 0, 7, 0, 0, 0, 0],
            [6, 0, 0, 1, 9, 5, 0, 0, 0],
            [0, 9, 8, 0, 0, 0, 0, 6, 0],
            [8, 0, 0, 0, 6, 0, 0, 0, 3],
            [4, 0, 0, 8, 0, 3, 0, 0, 1],
            [7, 0, 0, 0, 2, 0, 0, 0, 6],
            [0, 6, 0, 0, 0, 0, 2, 8, 0],
            [0, 0, 0, 4, 1, 9, 0, 0, 5],
            [0, 0, 0, 0, 8, 0, 0, 7, 9]
        ]
        print_board(board)  # Check if it prints the board without error


if __name__ == "__main__":
    unittest.main()